package basicsjava;

public class AdvancedLoop {
    public static void main(String[] args) {
        int []i={10,20,30,40,50};
        String []j={"abc","xyz"};

        for(int h:i)
        {
            System.out.println("This is Advanced for loop");
            System.out.println(h);
        }
        for(String k:j)
        {
            System.out.println(k);
        }
    }
}
