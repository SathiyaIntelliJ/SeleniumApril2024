package basicsjava;

public class LoopsExample {
    public static void main(String[] args) {
        String []a={"one","two","three"};
        for(int i=0;i<a.length;i++)
        {
            System.out.println(a[i]);
        }

        int []b={1,2,3,4,5,6};
        for(int c=0;c<b.length;c++)
        {
            System.out.println(b[c]);
        }


    }
}
